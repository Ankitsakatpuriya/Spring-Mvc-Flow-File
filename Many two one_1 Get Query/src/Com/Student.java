package Com;

public class Student {
	
	private int sid;
	private String sname;
	private Teacher sobject;
	
	
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public Teacher getSobject() {
		return sobject;
	}
	public void setSobject(Teacher sobject) {
		this.sobject = sobject;
	}
}
